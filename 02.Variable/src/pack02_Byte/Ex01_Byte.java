package pack02_Byte;

public class Ex01_Byte {
	public static void main(String[] args) {
		byte var1;
		var1 = 0;
		byte var2 = 10;
		System.out.println(var1);
		System.out.println(var2);
		// -128 ~ 127 표현가능
		// ※변수는 표현 범위를 가지기 때문에 해당하는 범위 안에서만 사용
		var1 = -128;
		var2 = 127;
		System.out.println(var1);
		System.out.println(var2);
		
		
	}

}
